public class EMP 
{
	int empid;
	String name;
	int age;
	double sal;
	int deptno;
	String job;
	public EMP(int empid, String name, int age, double sal, int deptno, String job) {
		super();
		this.empid = empid;
		this.name = name;
		this.age = age;
		this.sal = sal;
		this.deptno = deptno;
		this.job = job;
	}
	

}